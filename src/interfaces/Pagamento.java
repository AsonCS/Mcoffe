    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        if(txtClientePag.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Preencha nome!");
            id = 0;
        }else{
            try {
                id = 0;
                if(txtClientePag.getText().matches("[0-9]{1,3}")){
                    cr = cs.executeQuery("select * from comanda where num like '%"+txtClientePag.getText()+"%';");
                    if(cr.first()){
                        txtClientePag.setText(cr.getString("num"));
                        lblTotal.setText(cr.getString("lpre"));
                        tipo = COMANDA;
                        id = cr.getInt("id");
                        preencherJTabela("SELECT * FROM venCom where idCom = '"+id+"';");
                    }
                }else{
                    cr = cs.executeQuery("select * from cliente where nome like '%"+txtClientePag.getText()+"%';");
                    if(cr.first()){
                        txtClientePag.setText(cr.getString("nome"));
                        lblTotal.setText(cr.getString("cpre"));
                        tipo = CLIENTE;
                        id = cr.getInt("id");
                        preencherJTabela("SELECT * FROM venCli where idCli = '"+id+"';");
                    }
                }
            } catch (Exception e) {}
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

