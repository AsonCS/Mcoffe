    private void btnCalcularPagActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularPagActionPerformed
        // TODO add your handling code here: 
        if(!(txtClientePag.getText().isEmpty())){
            if(!(txtValorPago.getText().matches("([0-9]*)([.]?)([0-9]{0,2})")) || lblTotal.getText().isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Valor pago ou total invalido!");
            }else{
                try{
                    float x = Float.parseFloat(lblTotal.getText());
                    float y = Float.parseFloat(txtValorPago.getText());
                    if(y < x){            
                        x = x-y;
                        lblTroco.setText("R$ 0.00");                    
                    }else{
                        x = y-x;
                        lblTroco.setText("R$ "+String.format("%.2f", x));
                        x = 0;
                    }
                    String sql;
                    if(tipo == COMANDA)
                        sql = "UPDATE comanda SET lpre = "+x+" WHERE id = "+id+";";
                    else
                        sql = "UPDATE cliente SET cpre = "+x+" WHERE id = "+id+";";
                    cs.executeUpdate(sql);
                }catch(Exception e){
                    System.out.println(e);
                }
            }
        }
        btnPesquisarActionPerformed(evt);
    }//GEN-LAST:event_btnCalcularPagActionPerformed

