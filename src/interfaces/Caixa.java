    private void btnGravarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarCompraActionPerformed
        // TODO add your handling code here:
        if(idCli == 0){
            JOptionPane.showMessageDialog(rootPane, "Cliente não selecionado!");
        }else{
            if(!(txtValor.getText().matches("([0-9]*)([.]?)([0-9]{0,2})"))){
                JOptionPane.showMessageDialog(rootPane, "Valor invalido!");
            }else{
                try{
                    float pre = 0;
                    if(idPro != 0){
                        cr = cs.executeQuery("select * from estoque where cod like "+idPro+";");
                        if(cr.first()){                            
                            if(cr.getInt("qua") <= 0){
                                JOptionPane.showMessageDialog(rootPane, "Produto esgotado!");
                                pre = 0;
                            }else{                              
                                pre = cr.getFloat("pre");
                                cs.executeUpdate("update estoque set qua = qua - 1 where cod = "+idPro+";");                                
                            }
                        }else{
                            JOptionPane.showMessageDialog(rootPane, "Erro ao gravar!");
                            pre = 0;
                        }
                    }else if(!(txtValor.getText().isEmpty())){
                        pre = Float.parseFloat(txtValor.getText());
                        idPro = 0;
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "Preencha aumenos valor!");
                        pre = 0;
                    }
                    if(pre != 0){
                        String cli = "";
                        if(tipo == CLIENTE){
                            cs.executeUpdate("update cliente set cpre = cpre + "+pre+" WHERE id = "+idCli+";");
                            cr = cs.executeQuery("select * from cliente where id = "+idCli+";");
                            if(cr.first()){
                                lblDebitos.setText(cr.getString("cpre"));
                            }
                            cli = "Cli";
                        }else if(tipo == COMANDA){
                            cs.executeUpdate("update comanda set lpre = lpre + "+pre+" WHERE id = "+idCli+";");
                            cr = cs.executeQuery("select * from comanda where id = "+idCli+";");
                            if(cr.first()){
                                lblDebitos.setText(cr.getString("lpre"));
                            }
                            cli = "Com";
                        }
                        if(!cli.isEmpty()){
                            if(idPro != 0){
                                cs.executeUpdate("insert into venda (codPro, id"+cli+", dat, pre) values ('"+idPro+"', '"+idCli+"', '"+String.format("%tF", data)+"', '"+pre+"');");
                            }else{
                                cs.executeUpdate("insert into venda (id"+cli+", dat, pre) values ('"+idCli+"', '"+String.format("%tF", data)+"', '"+pre+"');");
                            }                            
                            preencherJTabela("SELECT * FROM ven"+cli+" where id"+cli+" = '"+idCli+"';");
                        }                                           
                    }
                }catch(Exception e){
                    JOptionPane.showMessageDialog(rootPane, "Erro ao gravar!"+e);
                    idPro = 0;
                }
            }
        }
    }//GEN-LAST:event_btnGravarCompraActionPerformed

    private void btPesquisarProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarProActionPerformed
        // TODO add your handling code here:        
        if(!(txtCodigo.getText().matches("[0-9]+")) && txtDescricao.getText().isEmpty()){
            txtCodigo.setText("");
            txtDescricao.setText("");
            txtValor.setText("");
            JOptionPane.showMessageDialog(rootPane, "Preencha corretamente os campos: Codigo ou Descrição!");
        }else{
            try{
                if(txtCodigo.getText().matches("[0-9]+")){
                    cr = cs.executeQuery("select * from estoque where cod like '"+txtCodigo.getText()+"';");
                    if(!cr.first()){
                        JOptionPane.showMessageDialog(rootPane, "Codigo não encontado!");
                    }
                }else{
                    cr = cs.executeQuery("select * from estoque where pro like '%"+txtDescricao.getText()+"%';");
                    if(!cr.first()){
                        JOptionPane.showMessageDialog(rootPane, "Produto não encontado!");
                    }
                }
                if(cr.first()){
                    txtCodigo.setText(cr.getString("cod"));
                    txtDescricao.setText(cr.getString("pro"));
                    txtValor.setText(cr.getString("pre"));
                    idPro = cr.getInt("cod");
                }else{
                    txtCodigo.setText("");
                    txtDescricao.setText("");
                    txtValor.setText("");
                    idPro = 0;
                }
            }catch(Exception e){}
        }
    }//GEN-LAST:event_btPesquisarProActionPerformed

