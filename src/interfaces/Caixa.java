    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        if(txtCliente.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Preencha cliente!");     
        }else{
            idCli = 0;
            try{
                if(txtCliente.getText().matches("[0-9]{1,3}")){
                    cr = cs.executeQuery("select * from comanda where num like '%"+txtCliente.getText()+"%';");
                    if(cr.first()){
                        txtCliente.setText(cr.getString("num"));
                        lblDebitos.setText("R$ "+cr.getString("lpre"));
                        tipo = COMANDA;
                        idCli = cr.getInt("id");
                        preencherJTabela("SELECT * FROM venCom where idCom = '"+idCli+"';");
                    }else{
                        idCli = 0;
                    }
                }else{
                    cr = cs.executeQuery("select * from cliente where nome like '%"+txtCliente.getText()+"%';");
                    if(cr.first()){
                        txtCliente.setText(cr.getString("nome"));
                        lblDebitos.setText("R$ "+cr.getString("cpre"));
                        tipo = CLIENTE;
                        idCli = cr.getInt("id");
                        preencherJTabela("SELECT * FROM venCli where idCli = '"+idCli+"';");
                    }else{
                        idCli = 0;
                    }
                }
            }catch(Exception e){}
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

